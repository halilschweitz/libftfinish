#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		size;
	char	*ptr;

	size = ft_strlen((char *)s1);
	ptr = malloc((size + 1) * sizeof(char));
	if (ptr == NULL)
		return (0);
	ft_memcpy(ptr, s1, size);
	ptr[size] = '\0';
	return (ptr);
}
